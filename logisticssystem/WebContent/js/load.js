function start_load() {
    var cloth = document.getElementById("cloth");
    var div_load = document.getElementById("div_load");
    cloth.style.display = "block";
    div_load.style.display = "block";
}
function stop_load() {
    var cloth = document.getElementById("cloth");
    var div_load = document.getElementById("div_load");
    cloth.style.display = "none";
    div_load.style.display = "none";
}